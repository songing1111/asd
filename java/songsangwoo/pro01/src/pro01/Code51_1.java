package pro01;

public class Code51_1 {

	public static void main(String[] args) {
		int score = 80;
		char grade ;
	
		if (score == 90)
			grade = 'A';
		else if (score == 80)
			grade = 'B';
		else if (score == 70)
			grade = 'C';
		else
			grade = 'D';
		
	System.out.println("grade : " + grade);
	
	}

}
